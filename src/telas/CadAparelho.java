    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jcliente.addItem("Selecione");
        String cliente = "select nomeCliente from cliente where nomeCliente is not null";
        try {
            linguagem = conexao.prepareStatement(cliente);
            linha = linguagem.executeQuery();
            while(linha.next()){
                jcliente.addItem(linha.getString("nomeCliente"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Erro "+ e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened

