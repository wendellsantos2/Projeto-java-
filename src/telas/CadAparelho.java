    void limpar(){
        jnome.setText("");
        jmarca.setText("");
        jdescricao.setText("");
        jvalor.setText("");
        jcliente.setSelectedItem("Selecione");
        jcodigo.setText("");
        jnome.requestFocus();
    }
    
    void novo(){
        jnome.setEnabled(true);
        jmarca.setEnabled(true);
        jdescricao.setEnabled(true);
        jvalor.setEnabled(true);
        jcliente.setEnabled(true);
        jsalvar.setEnabled(true);
        jnome.requestFocus();
    }
    
    void cadastrar(){
        int id = Integer.parseInt(jcodigo.getText());
        double valor = Double.parseDouble(jvalor.getText().replace(",","."));
        try {
            Aparelho aparelho = new Aparelho();
            Cliente cliente = new Cliente();
            
            aparelho.setNomeAparelho(jnome.getText());
            aparelho.setMarca(jmarca.getText());
            aparelho.setDescricao(jdescricao.getText());
            aparelho.setValor(valor);
            cliente.setIdCliente(id);
            aparelho.setCliente(cliente); //aqui acontece a chave estrangeira(FK)
            
            AparelhoDao dao = new AparelhoDao();
            dao.adicionar(aparelho);
            JOptionPane.showMessageDialog(null, "Aparelho Cadastrado");
            limpar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Erro ao inserir");
        }
    }

